#include <minishell.h>

void remove_quotes(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] == '\1' || str[i] == '\2')
			i++;
		else
			str[j++] = str[i++];
	}
	str[j] = '\0';
}